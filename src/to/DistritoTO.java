package to;

public class DistritoTO {
    private int id_distrito;
    private String nomb_dist;
    private String obsv_dist;

    public int getId_distrito() {
        return id_distrito;
    }

    public void setId_distrito(int id_distrito) {
        this.id_distrito = id_distrito;
    }

    public String getNomb_dist() {
        return nomb_dist;
    }

    public void setNomb_dist(String nomb_dist) {
        this.nomb_dist = nomb_dist;
    }

    public String getObsv_dist() {
        return obsv_dist;
    }

    public void setObsv_dist(String obsv_dist) {
        this.obsv_dist = obsv_dist;
    }
}
